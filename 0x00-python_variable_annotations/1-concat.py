#!/usr/bin/env python3
"""
This module defines a function `concat` that concatenates two strings.
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings and returns the result.

    Args:
        str1 (str): First string.
        str2 (str): Second string.

    Returns:
        str: Concatenated result of str1 and str2.
    """

    return str1 + str2
